// ============================================================
// ПРИКЛАД: Метод cy.get() - основні можливості
// ОПИС: Демонстрація різних способів використання cy.get()
// ============================================================

// Приклад 1: Базове використання з CSS селектором
// cy.get() автоматично чекає на появу елемента (до 4 секунд)
cy.get('.new-todo')
  .should('be.visible')

// Приклад 2: Пошук за різними типами селекторів
cy.get('.new-todo')        // За класом
cy.get('h1')               // За тегом
cy.get('[type="text"]')    // За атрибутом

// Приклад 3: Пошук кількох елементів
cy.get('.todo-list li')
  .should('have.length', 3)

// Приклад 4: cy.get() з опціями
// Встановлення таймауту 10 секунд
cy.get('.new-todo', { timeout: 10000 })
  .should('be.visible')

// Приклад 5: Комбіновані селектори
cy.get('input.new-todo[type="text"]')
  .should('exist')

// Приклад 6: Псевдокласи
cy.get('.todo-list li:first-child')
  .should('contain', 'Перша задача')
