// ============================================================
// ПРИКЛАД: Спеціальні методи
// ОПИС: Демонстрація спеціальних методів для роботи з елементами
// ============================================================

// Приклад 1: focused() - елемент у фокусі
cy.get('.new-todo').focus()
cy.focused()
  .should('have.class', 'new-todo')

// Приклад 2: invoke() - виклик методів
cy.get('.new-todo')
  .invoke('val', 'Текст')
  .should('have.value', 'Текст')

// Приклад 3: its() - отримання властивостей
cy.get('.new-todo')
  .its('length')
  .should('eq', 1)

// Приклад 4: title() - заголовок сторінки
cy.title()
  .should('include', 'React')
