// ============================================================
// ПРИКЛАД: XPath Селектори - базовий синтаксис
// ОПИС: Демонстрація використання XPath селекторів
// ВАЖЛИВО: Потрібно встановити cypress-xpath та додати import в e2e.js
// ============================================================

// Приклад 1: Базовий XPath селектор
// Подвійний слеш // означає "шукати на будь-якому рівні DOM дерева"
cy.xpath('//h1')
  .should('contain', 'todos')

// Приклад 2: XPath з атрибутом
// @type - синтаксис XPath для посилання на атрибути
cy.xpath('//input[@type="text"]')
  .should('be.visible')

// Приклад 3: XPath з текстовим вмістом
// contains() - функція XPath для часткового співпадіння тексту
cy.xpath('//h1[contains(text(), "todos")]')
  .should('exist')

// Приклад 4: XPath з комбінацією умов
// Квадратні дужки [] використовуються для умов
cy.xpath('//li[contains(@class, "todo")][1]')
  .should('exist')

// Приклад 5: XPath для навігації по DOM
// Подвійний слеш // всередині означає "будь-який нащадок"
cy.xpath('//li[contains(text(), "Задача")]//input[@type="checkbox"]')
  .should('exist')

// Приклад 6: XPath з функцією position()
// position() - функція XPath, яка повертає позицію елемента
cy.xpath('//li[position()=1]')
  .should('contain', 'Перша задача')

// Приклад 7: XPath з функцією last()
// last() - функція XPath для вибору останнього елемента
cy.xpath('//li[contains(@class, "todo")][last()]')
  .should('contain', 'Остання задача')

// Приклад 8: XPath з функцією starts-with()
// starts-with() - функція для перевірки початку рядка
cy.xpath('//input[starts-with(@placeholder, "What")]')
  .should('exist')

// Приклад 9: XPath з функцією contains() для атрибутів
// contains() працює не тільки з текстом, але й з атрибутами
cy.xpath('//input[contains(@class, "new-todo")]')
  .should('exist')

// Приклад 10: XPath з операторами порівняння
// Можна використовувати оператори: >, <, >=, <=, =
cy.xpath('//li[position()>1]')
  .should('have.length', 2)

