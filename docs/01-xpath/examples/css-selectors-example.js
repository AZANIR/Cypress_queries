// ============================================================
// ПРИКЛАД: CSS Селектори - базові типи
// ОПИС: Демонстрація основних типів CSS селекторів
// ============================================================

// Приклад 1: Пошук за класом
// Крапка (.) перед назвою означає клас CSS
cy.get('.new-todo')
  .should('be.visible')

// Приклад 2: Пошук за ID
// Решітка (#) перед назвою означає ID
// Увага: у реальних проектах уникайте ID для тестів
cy.get('#my-element-id')
  .should('exist')

// Приклад 3: Пошук за HTML тегом
// Просто назва тега без додаткових символів
cy.get('h1')
  .should('contain', 'todos')

// Приклад 4: Пошук за атрибутом
// Квадратні дужки [] використовуються для атрибутів
cy.get('[type="text"]')
  .should('be.visible')

// Приклад 5: Комбінація тега та класу
// 'input.new-todo' - input елемент з класом new-todo
cy.get('input.new-todo')
  .should('exist')

// Приклад 6: Комбінація тега, класу та атрибута
// Більш специфічний селектор для точнішого вибору
cy.get('input.new-todo[type="text"]')
  .should('be.visible')

// Приклад 7: Вкладені елементи
// Пробіл між селекторами означає "будь-який нащадок"
cy.get('.todo-list li')
  .should('have.length', 3)

// Приклад 8: Прямі дочірні елементи
// Символ > означає "прямий дочірній елемент"
cy.get('.todo-list > li')
  .should('have.length', 3)

// Приклад 9: Псевдокласи для позиції
// :first-child - перший дочірній елемент
cy.get('.todo-list li:first-child')
  .should('contain', 'Перша задача')

// Приклад 10: Псевдокласи для позиції
// :last-child - останній дочірній елемент
cy.get('.todo-list li:last-child')
  .should('contain', 'Остання задача')

// Приклад 11: Псевдокласи для позиції
// :nth-child(n) - n-й дочірній елемент (нумерація з 1!)
cy.get('.todo-list li:nth-child(2)')
  .should('contain', 'Друга задача')

