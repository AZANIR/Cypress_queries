// ============================================================
// ПРИКЛАД: Методи first() та last()
// ОПИС: Демонстрація вибору першого та останнього елемента
// ============================================================

// Приклад 1: first() - перший елемент
cy.get('.todo-list li')
  .first()
  .should('contain', 'Перша задача')

// Приклад 2: last() - останній елемент
cy.get('.todo-list li')
  .last()
  .should('contain', 'Остання задача')

// Приклад 3: first() та last() для взаємодії
cy.get('.todo-list li')
  .first()
  .find('.toggle')
  .check()

cy.get('.todo-list li')
  .last()
  .find('.toggle')
  .check()
