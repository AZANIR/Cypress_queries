// Приклад 1: data-атрибути (найкраще)
// У реальних проектах додавайте data-cy до елементів
cy.get('[data-cy="todo-input"]')
  .should('be.visible')

// Приклад 2: Семантичні класи (добре)
cy.get('.new-todo')
  .should('exist')

// Приклад 3: Комбіновані селектори (прийнятно)
cy.get('input.new-todo[type="text"]')
  .should('exist')

// Приклад 4: contains() для тексту (прийнятно)
cy.contains('.todo-list li', 'Задача')
  .should('exist')

